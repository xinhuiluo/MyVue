<template>
    <!--    头部区域-->
    <el-container class="home-container">
        <el-header>
            <div>
                <img src="../assets/电商物品.png" alt="">
                <span>测试管理系统</span>
            </div>
            <el-button type="info" @click="logout">退出</el-button>
        </el-header>
        <!--        页面主题区域-->
        <el-container>
            <!--            侧边栏-->
            <el-aside :width="isCollapse ? '64px':'200px'">
                <div class="toggle-button" @click="toggleCollapse">!!!</div>
                <!--                侧边栏区域  defoult-active菜单高亮显示-->
                <el-menu background-color="#373d41" text-color="#fff" active-text-color="#409bff"
                         :unique-opened="true" :collapse="isCollapse" :collapse-transition="false"
                         router :defoult-active="activePath">
                    <!--                    一级菜单-->
                    <el-submenu :index="item.id +''" v-for="item in menulist" :key="item.id">
                        <template slot="title">
                            <i class="el-icon-location"></i>
                            <span>{{item.authName}}</span>
                        </template>
                        <!--                        二级菜单-->
                        <el-menu-item :index="'/'+subItem.path" v-for="subItem in item.children" :key="subItem.id">
                            <template slot="title" @click="saveNavState('/'+subItem.path)">
                                <i class="el-icon-menu"></i>
                                <span>{{subItem.authName}}</span>
                            </template>
                        </el-menu-item>
                    </el-submenu>
                </el-menu>
            </el-aside>
            <!--            右侧内容-->
            <el-main>
                <!--                路由占位符-->
                <router-view></router-view>
            </el-main>
        </el-container>
    </el-container>
</template>

<script>
    export default {
        data() {
            return {
                // 左侧菜单数据
                menulist: [],
                // 是否折叠
                isCollapse: false,
                // 被激活的链接地址（高亮）
                activePath: '',
            }
        },
        created() {
            this.getMenuList()
            this.activePath = window.sessionStorage.getItem('activePath')
        },
        methods: {
            logout() {
                window.sessionStorage.clear()
                this.$router.push('/login')
            },
            // 获取所以菜单
            async getMenuList() {
                const {data: res} = await this.$http.get('https://www.liulongbin.top:8888/api/private/v1/menus')
                console.log(res)
                if (res.meta.status !== 200) return this.$message.error(res.meta.msg)
                console.log(res.data)
                this.menulist = res.data
            },
            // 点击按钮切换菜单折叠，展开
            toggleCollapse() {
                this.isCollapse = !this.isCollapse
            },
            // 保存链接激活状态
            saveNavState(activePath) {
                window.sessionStorage.setItem('activePath', activePath)
                this.activePath = activePath
            },
        }
    }
</script>

